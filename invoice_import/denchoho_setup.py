"""
電子帳簿保存法対応セットアップモジュール
共有ドライブの設定ガイド表示と事務処理規程テンプレートの生成を行う。

参考:
- 藤井翔吾氏: ファイル名だけでなくメタ項目の必須化が重要
- shione_and_shion氏: 共有ドライブの「投稿者」権限で改ざん防止
- 国税庁: 事務処理規程テンプレート
"""

import os
from datetime import datetime
from pathlib import Path


def show_shared_drive_guide():
    """共有ドライブの電帳法対応セットアップガイドを表示する"""
    guide = """
================================================================================
  Google 共有ドライブ 電子帳簿保存法対応セットアップガイド
================================================================================

【前提条件】
  Google Workspace Business Plus以上（Vault機能あり）を推奨。
  Business Standard以下の場合は事務処理規程の策定で代替可能。

【手順1】共有ドライブの作成
  1. Google Driveで「共有ドライブ」→「新しい共有ドライブ」
  2. 名前: 「電子帳簿保存_顧客名」など
  3. この共有ドライブを本システムのwatch_folder_idに設定する

【手順2】アクセス権限の設定（真実性の確保）
  ┌─────────────────────────────────────────────┐
  │ 管理者（所長など）    → 「管理者」権限        │
  │ 担当スタッフ          → 「投稿者」権限        │
  │ 顧客（資料アップ用）  → 「投稿者」権限        │
  └─────────────────────────────────────────────┘

  「投稿者」権限のポイント:
  - ファイルのアップロード: 可能
  - 自分がアップしたファイルの編集: 可能
  - 他人のファイルの編集・削除: 不可
  → 改ざん防止の要件を満たせる

【手順3】フォルダ構成（本システムが自動作成）
  共有ドライブ/
  ├── (ルート)         ← 新しい請求書・領収書を置く場所
  ├── 処理済み/        ← 処理後に自動移動
  │   ├── 2026年01月/  ← 年月別に自動整理
  │   ├── 2026年02月/
  │   └── ...
  └── MFインポート/    ← 生成CSVの保存先

【手順4】Vault保持ルールの設定（Business Plus以上）
  1. admin.google.com → アプリ → Google Vault
  2. 保持 → カスタムルール → 作成
  3. 対象: ドライブ → 該当の共有ドライブ
  4. 保持期間: 2555日（7年）
  → 削除してもVaultに7年間保持される

【手順5】Vault未使用の場合（事務処理規程で代替）
  本システムの --generate-regulations コマンドで
  事務処理規程のテンプレートを生成できます。

  python main.py --generate-regulations --firm-name "○○税理士事務所"

================================================================================
"""
    print(guide)


def generate_regulations(firm_name: str, output_path: str, representative: str = ""):
    """電子帳簿保存法の事務処理規程テンプレートを生成する

    国税庁のサンプルをベースに、本システムの運用に合わせた内容で生成する。
    """
    today = datetime.now().strftime("%Y年%m月%d日")
    rep = representative or "（代表者名を記入）"

    content = f"""電子取引データの訂正及び削除の防止に関する事務処理規程

（目的）
第1条　本規程は、電子計算機を使用して作成する国税関係帳簿書類の保存方法等の
特例に関する法律第7条に定められた電子取引の取引情報に係る電磁的記録の保存に
ついて、適正かつ円滑な運用を確保するために必要な事項を定めることを目的とする。

（適用範囲）
第2条　本規程は、{firm_name}の全ての役職員（パート・アルバイトを含む）に
適用する。また、本事務所が記帳代行を行う全ての顧問先の電子取引データに
ついても本規程に準じて管理する。

（管理責任者）
第3条　本規程に基づく電子取引データの管理責任者は、{rep}とする。

（電子取引の範囲）
第4条　本規程における電子取引とは、以下の取引をいう。
  (1) 電子メールにより受領した請求書・領収書等
  (2) クラウドサービスからダウンロードした請求書・領収書等
  (3) スキャナで読み取った紙の請求書・領収書等の電子データ
  (4) ウェブサイト上で発行・受領した電子的な取引書類
  (5) Google Driveに保存された上記に該当する全てのファイル

（保存方法）
第5条　電子取引データの保存は、以下の方法により行う。
  (1) Google Driveの共有ドライブに保存する
  (2) 保存時のファイル名は以下の形式とする
      「取引年月日_取引先名_金額円_元のファイル名.拡張子」
      （例: 20260306_株式会社ABC_50000円_invoice.pdf）
  (3) 処理済みのファイルは年月別フォルダに自動整理される
  (4) 本システム（請求書インポートシステム）による処理ログを
      processing_log.json として顧客別に保持する

（検索要件への対応）
第6条　電子取引データは、以下の項目により検索可能な状態で保存する。
  (1) 取引年月日（ファイル名およびフォルダ構成による）
  (2) 取引金額（ファイル名による）
  (3) 取引先名（ファイル名による）
  (4) 上記項目の範囲指定および組み合わせ検索は、
      Google Driveの検索機能により対応する

（訂正・削除の制限）
第7条　保存された電子取引データの訂正・削除は、原則として禁止する。
  2　やむを得ず訂正・削除を行う場合は、以下の手続きに従う。
    (1) 訂正・削除の理由を明確にすること
    (2) 管理責任者の承認を得ること
    (3) 訂正・削除の日時、担当者、理由を記録すること
    (4) 訂正前のデータを保存しておくこと
  3　共有ドライブのアクセス権限を「投稿者」に設定し、
    管理者以外による他者のファイルの編集・削除を技術的に制限する。

（保存期間）
第8条　電子取引データの保存期間は、法人にあっては7年間、
個人事業主にあっては原則5年間（青色申告者は7年間）とする。

（顧問先データの管理）
第9条　顧問先の電子取引データは、顧問先ごとに独立したフォルダで管理し、
他の顧問先のデータと混在しないようにする。
  2　記帳代行システムにおける顧客別の仕訳履歴（journal_history.json）
    および処理ログ（processing_log.json）は、監査証跡として保持する。

（教育・周知）
第10条　管理責任者は、本規程の内容を全ての対象者に周知し、
適切な運用が行われるよう必要な教育を行う。

附則
  本規程は、{today}から施行する。

{firm_name}
管理責任者: {rep}
"""

    os.makedirs(os.path.dirname(output_path) or ".", exist_ok=True)
    with open(output_path, "w", encoding="utf-8") as f:
        f.write(content)

    print(f"事務処理規程を生成しました: {output_path}")
    print("※ このテンプレートを自事務所の実態に合わせて修正してください。")
    print("※ 国税庁の公式サンプルも併せて参照してください:")
    print("  https://www.nta.go.jp/law/joho-zeikaishaku/sonota/jirei/0021006-031.htm")

    return output_path
